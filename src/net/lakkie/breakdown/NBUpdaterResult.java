package net.lakkie.breakdown;

public class NBUpdaterResult
{

	private String recommendedVersionName;
	private String recommendedVersionID;

	public String getRecommendedVersionName()
	{
		return recommendedVersionName;
	}

	public void setRecommendedVersionName(String recommendedVersionName)
	{
		this.recommendedVersionName = recommendedVersionName;
	}

	public String getRecommendedVersionID()
	{
		return recommendedVersionID;
	}

	public void setRecommendedVersionID(String recommendedVersionID)
	{
		this.recommendedVersionID = recommendedVersionID;
	}

}
